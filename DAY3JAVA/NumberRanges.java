package DAY3JAVA;
public class NumberRanges {
    public static void main(String[] args) {

        int n1 = -124;
        while (n1 >= -129) {
            System.out.println(n1);
            n1--;
        }

        int n2 = 79;
        while (n2 <= 84) {
            System.out.println(n2);
            n2++;
        }

        int n3 = -110;
        while (n3 <= -100) {
            System.out.println(n3);
            n3++;
        }

        int n4 = 0;
        while (n4 >= -9) {
            System.out.println(n4);
            n4--;
        }
    }
}
