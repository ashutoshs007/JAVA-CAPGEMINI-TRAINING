// WAJP TO PRINT CHARACTERS FROM THE RANGE
// A to M
// x to m
package DAY3JAVA;

public class PrintChar {
    public static void main(String[] args) {

        char a = 'A';
        while (a <= 'M') {
            System.out.println(a);
            a++;
        }

        char b = 'x';
        while (b >= 'm') {
            System.out.println(b);
            b--;
        }
    }
}

